# 交叉编译目标三元组
# 常见值:
#   aarch64-linux-gnu  - ARM64 (Android, Raspberry Pi 4等)
#   arm-linux-gnueabihf - ARM32 (树莓派2/3等)
#   x86_64-linux-gnu   - x86_64 Linux
export TARGET_HOST="aarch64-linux-gnu"

# 宿主机三元组
# 通常不需要修改
export BUILD_HOST="x86_64-linux-gnu"

